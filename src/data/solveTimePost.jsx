<p>For two years, he had an open GitHub issue for a feature that would allow developers to slow down the network specifically for the simulator, without affecting the rest of the Mac’s connection. “Every single answer you would basically find on Stack Overflow,” he recalls. “But I created that issue like, ‘Okay, I want to create a solution that works just for the simulator.’ And that issue has been open for two years. I just couldn’t find the answer.”</p>
